    /**
     * This function is used to check whether email already exist or not
     */
    function checkEmailExists()
    {
        $userId = $this->input->post("userId");
        $email = $this->input->post("email");

        if(empty($userId)){
            $result = $this->user_model->checkEmailExists($email);
        } else {
            $result = $this->user_model->checkEmailExists($email, $userId);
        }

        if(empty($result)){ echo("true"); }
        else { echo("false"); }
    }
    
    /**
     * This function is used to add new user to the system
     */
    function addNewUser()
    {
        if($this->isAdmin() == TRUE)
        {
            $this->loadThis();
        }
        else
        {
            $this->load->library('form_validation');
            
            $this->form_validation->set_rules('fname','Nome Completo','trim|required|max_length[128]');
            $this->form_validation->set_rules('login','Login','trim|required|max_length[128]');
            $this->form_validation->set_rules('email','E-mail','trim|required|valid_email|max_length[128]');
            $this->form_validation->set_rules('password','Senha','required|max_length[20]');
            $this->form_validation->set_rules('cpassword','Confirma Senha','trim|required|matches[password]|max_length[20]');
            $this->form_validation->set_rules('role','Regras','trim|required|numeric');
            // $this->form_validation->set_rules('mobile','Celular','required|min_length[10]');
            $this->form_validation->set_rules('optradio','Empregado Empresa','required');
            
            if($this->form_validation->run() == FALSE)
            {
                $this->addNew();
            }
            else
            {
                $name = ucwords(strtolower($this->security->xss_clean($this->input->post('fname'))));
                $email = strtolower($this->security->xss_clean($this->input->post('email')));
                $login = $this->input->post('login');
                $password = $this->input->post('password');
                $roleId = $this->input->post('role');
                $mobile = $this->security->xss_clean($this->input->post('mobile'));
                
                $daEmpresa = $this->input->post('optradio');


                $userInfo = array('email'=>$email, 'password'=>getHashedPassword($password), 'login'=>$login, 
                    'roleId'=>$roleId, 'name'=> ucwords($name), 'mobile'=>$mobile, 'createdBy'=>$this->vendorId, 
                    'createdDtm'=>date('Y-m-d H:i:s'), 'empregado'=> $daEmpresa);
                
                $this->load->model('user_model');
                $result = $this->user_model->addNewUser($userInfo);
                
                if($result > 0)
                {
                    $this->session->set_flashdata('success', 'Usuário(a) '.$name.' cadastrado(a) com sucesso.');
                }
                else
                {
                    $this->session->set_flashdata('error', 'Falha ao cadastrar novo usuário(a)');
                }
                
                redirect('addNew');
            }
        }
    }

    
